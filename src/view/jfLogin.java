    public boolean validarCampos() {
        if (jtUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite o nome do usu√°rio.");
            jtUsuario.requestFocus();
            return false;
        }
        if (jpSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Digite a senha do usu√°rio.");
            jpSenha.requestFocus();
            return false;
        }
        return true;
    }

    public void limparCampos() {
        jtUsuario.setText("");
        jpSenha.setText("");
    }
