    private void jbSalvarAlteracoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarAlteracoesActionPerformed
        try {
            if (validarCampos()) {
                if (preencherObjeto()) {
                    if (DAO.editarUsuario(objUsuario)) {
                        JOptionPane.showMessageDialog(this, "Alterações salvas com sucesso!");
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Não foi possivel fazer as alterações!");
                        limparCampos();
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_jbSalvarAlteracoesActionPerformed

