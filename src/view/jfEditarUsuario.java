    private void jbPreencherCamposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPreencherCamposActionPerformed
        try {
            if (jtPesquisaUsuario.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Entre com o nome do usuario!");
                jtPesquisaUsuario.requestFocus();
            } else {
                objUsuario = DAO.pesquisar(jtPesquisaUsuario.getText());

                if (objUsuario == null) {
                    JOptionPane.showMessageDialog(this, "Usuario nao encontrado!");
                } else {
                    jtNomUsuario.setText(objUsuario.getNomUsuario());
                    jtDesEmail.setText(objUsuario.getDesEmail());
                    jtDesLogin.setText(objUsuario.getDesLogin());
                    jpSenha.setText(objUsuario.getDesSenha());
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar!" + ex.getMessage());
        }
    }//GEN-LAST:event_jbPreencherCamposActionPerformed

